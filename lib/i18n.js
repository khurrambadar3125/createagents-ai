// RTL languages
const RTL_LANGS = ['ar', 'ur']

const I18nContext = createContext()

export function I18nProvider({ children }) {
  const [locale, setLocale] = useState('en')

  useEffect(() => {
    const saved = localStorage.getItem('ca_lang')
    if (saved && TRANSLATIONS[saved]) {
      setLocale(saved)
    } else {
      // Auto-detect from browser
      const browserLang = navigator.language?.split('-')[0]
      if (TRANSLATIONS[browserLang]) setLocale(browserLang)
    }
  }, [])

  useEffect(() => {
    localStorage.setItem('ca_lang', locale)
    document.documentElement.lang = locale
    document.documentElement.dir = RTL_LANGS.includes(locale) ? 'rtl' : 'ltr'
  }, [locale])

  function t(key, params = {}) {
    let text = TRANSLATIONS[locale]?.[key] || TRANSLATIONS.en[key] || key
    Object.entries(params).forEach(([k, v]) => {
      text = text.replace(`{${k}}`, v)
    })
    return text
  }

  const isRtl = RTL_LANGS.includes(locale)

  return (
    <I18nContext.Provider value={{ locale, setLocale, t, isRtl, languages: Object.entries(TRANSLATIONS).map(([code, t]) => ({ code, name: t.lang_name })) }}>
      {children}
    </I18nContext.Provider>
  )
}

export function useI18n() {
  return useContext(I18nContext)
}
